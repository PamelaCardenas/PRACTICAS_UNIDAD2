/*
 * Esta clase servirá para guardar las coordenadas de los puntos en los
 * que se encuentran las figuras que se van a dibujar
 */
package figuras;

/**
 *
 * @author Karla Pamela Cárdenas Leyva 18550338
 */
public class Punto {

    //Atributos X y Y para las coordenadas
    private int X;
    private int Y;

    //Métodos get y set de X y Y
    public int getX() {
        return X;
    }

    public void setX(int X) {
        this.X = X;
    }

    public int getY() {
        return Y;
    }

    public void setY(int Y) {
        this.Y = Y;
    }

    
}
